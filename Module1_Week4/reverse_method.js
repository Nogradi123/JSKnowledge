/**

    - Reverse method reverses an array in place. The first element becomes the last and the last array element becomes the first 

    - The reverse method transposes the elements of the calling array in place, mutating the array, and returning a reference to it 

    array.reverse();

*/

const arr1 = ['one', 'two', 'three'];
const arr2 = arr1.reverse();

console.log(arr1);
console.log(arr2);


